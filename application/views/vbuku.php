<h2> Apa itu Framework ? </h2>
<p> Framework merupakan sebuah kerangka kerja atau sekumpulan file - file yang sudah ter-include, yang mana 
di dalam file tersebut terdapat perintah kode program dan fungsi dasar untuk melakukan tugas tertentu. </p>

<h3> Jadi, Framework CodeIgniter adalah ... </h3>
<p> Sebuah framework yang dibuat menggunakan bahasa pemrograman PHP yang bertujuan untuk memudahkan para programer web
untuk membuat atau mengembangkan aplikasi berbasis web. </p>
